public class Activite {
 {
    private int id;
    private string nom;

    public Activite(int id, string nom) {
        this.id = id;
        this.nom = nom;
    }
    public int Id {
        get { return id; }
        set { id = value; }
    }
    public string Nom {
        get { return nom; }
        set { nom = value; }
    }
}
    }
}